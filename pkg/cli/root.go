package cli

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "glypto",
	Short: "A CLI tool for scraping webpage metadata",
	Long: `Glypto is a CLI tool for scraping metadata from websites using a provider-based architecture.

It extracts metadata including titles, descriptions, images, Open Graph data,
Twitter Cards, and RSS/Atom feeds from web pages.`,
	Version: "0.1.0",
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

